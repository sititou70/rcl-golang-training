	if comma("123456789") != "123,456,789" {
		t.Fail()
	}
	if comma("+123456789") != "+123,456,789" {
		t.Fail()
	}
	if comma("-123456789") != "-123,456,789" {
		t.Fail()
	}
	if comma("12345678.9") != "12,345,678.9" {
		t.Fail()
	}
	if comma("+12345678.9") != "+12,345,678.9" {
		t.Fail()
	}
	if comma("-12345678.9") != "-12,345,678.9" {
		t.Fail()
	}
	if comma("1234567.89") != "1,234,567.89" {
		t.Fail()
	}
	if comma("+1234567.89") != "+1,234,567.89" {
		t.Fail()
	}
	if comma("-1234567.89") != "-1,234,567.89" {
		t.Fail()
	}
	if comma("123456.789") != "123,456.789" {
		t.Fail()
	}
	if comma("+123456.789") != "+123,456.789" {
		t.Fail()
	}
	if comma("-123456.789") != "-123,456.789" {
		t.Fail()
	}
	if comma("12345.6789") != "12,345.6789" {
		t.Fail()
	}
	if comma("+12345.6789") != "+12,345.6789" {
		t.Fail()
	}
	if comma("-12345.6789") != "-12,345.6789" {
		t.Fail()
	}
